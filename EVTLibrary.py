def get_data(start: str = "2000-01-01", end: str = "2026-03-31") -> pd.DataFrame:
    """
    Download stock data, clean it and return a DataFrame of daily losses.

    Parameters
    ----------

    start : str
        Start date in format 'YYYY-MM-DD'.
    end : str
        End date in format 'YYYY-MM-DD'.

    Returns
    -------
    loss_df : pd.DataFrame
        DataFrame containing daily log-losses for each stock.
    """
    
    if not isinstance(start, str) or not isinstance(end, str):
        raise TypeError("start and end must be strings in format 'YYYY-MM-DD'")

    
    data = {
        "Kinnevik B": yf.download("KINV-B.ST", start=start, end=end, interval="1d", auto_adjust=True, progress=False),
        "Investor B": yf.download("INVE-B.ST", start=start, end=end, interval="1d", auto_adjust=True, progress=False),
        "SEB A": yf.download("SEB-A.ST", start=start, end=end, interval="1d", auto_adjust=True, progress=False),
        "Nordea Bank": yf.download("NDA-SE.ST", start=start, end=end, interval="1d", auto_adjust=True, progress=False),
        "Castellum": yf.download("CAST.ST", start=start, end=end, interval="1d", auto_adjust=True, progress=False),
        "Skanska B": yf.download("SKA-B.ST", start=start, end=end, interval="1d", auto_adjust=True, progress=False),
        "ABB": yf.download("ABB.ST", start=start, end=end, interval="1d", auto_adjust=True, progress=False),
        "Atlas Copco A": yf.download("ATCO-A.ST", start=start, end=end, interval="1d", auto_adjust=True, progress=False),
        "Boliden": yf.download("BOL.ST", start=start, end=end, interval="1d", auto_adjust=True, progress=False),
        "SCA B": yf.download("SCA-B.ST", start=start, end=end, interval="1d", auto_adjust=True, progress=False)
    }

    data_clean = {}
    for k, df in data.items():
        data_clean[k] =  df.copy().dropna() 

    for df in data_clean.values():
        df["lgr"] = np.log(df["Close"]).diff()

    loss_df = pd.DataFrame()
    for k in data_clean.keys():
        loss_df[k] = -data_clean[k]["lgr"]

    loss_df.dropna(inplace=True)  
   
    return loss_df


def createPortfolio(shares, weights: np.ndarray):
    """
    Create a portfolio from shares and weights.

    Parameters
    ----------
    shares : iterable
        Iterable of return/loss series.
    weights : np.ndarray
        Array of portfolio weights.

    Returns
    -------
    portfolio : pd.Series
        Weighted portfolio series.
    """
    portfolio = sum(share * weight for share, weight in zip(shares, weights))
    return pd.DataFrame(portfolio, columns=["Portfolio"])



def get_extreme_data(thresholds: list, data: pd.DataFrame, exceedances: bool = False) -> tuple[dict[str, float], pd.DataFrame]:
    """
    Filter the dataset with given thresholds using POT.

    Parameters
    ----------
    thresholds : list
        Threshold values, one per column in data.
    data : pd.DataFrame
        DataFrame generated by get_data().
    exceedances : bool
        If True, return exceedances x - u for x > u.
        If False, return the observations x for x > u.

    Returns
    -------
    u : dict
        Dictionary mapping stock names to threshold values.
    extreme_data : pd.DataFrame
        DataFrame containing threshold exceedances or extreme observations.
    """
    if not isinstance(thresholds, list):
        raise TypeError("thresholds must be a list")
    elif not isinstance(data, pd.DataFrame):
        raise TypeError("data must be a DataFrame")
    elif not isinstance(exceedances, bool):
        raise TypeError("exceedances must be a bool")

    if len(thresholds) != len(data.columns):
        raise ValueError("Number of thresholds must match number of columns in data")

    u = dict(zip(data.columns, thresholds))
    extreme_data = data.copy()

    for k in extreme_data.columns:
        if exceedances:
            extreme_data[k] = (extreme_data.loc[extreme_data[k] > u[k], k] - u[k]).astype(float)
        else:
            extreme_data[k] = extreme_data.loc[extreme_data[k] > u[k], k].astype(float)

    return u, extreme_data



def get_cluster_maxima(thresholds: dict[str, float], data: pd.DataFrame, r: int) -> tuple[dict[str, pd.DataFrame], dict[str, pd.DataFrame]]:
    """
    Identify exceedance clusters using r-runs-declustering and return
    the maximum exceedance from each cluster.

    Parameters
    ----------
    thresholds : dict[str, float]
        Threshold values by stock.
    data : pd.DataFrame
        DataFrame of losses.
    r : int
        Maximum gap in calendar days before a new cluster starts.

    Returns
    -------
    cluster_maxima : dict[str, pd.DataFrame]
        Dictionary mapping each stock to a DataFrame containing the
        maximum exceedance from each cluster.

    clustered_exceedances : dict[str, pd.DataFrame]
        Dictionary mapping each stock to a DataFrame containing all
        exceedances together with their assigned cluster.

    """
    if not isinstance(thresholds, dict):
        raise TypeError("thresholds must be a dictionary")
    elif not isinstance(data, pd.DataFrame):
        raise TypeError("data must be a DataFrame")
    elif not isinstance(r, int):
        raise TypeError("r must be an int")

    cluster_maxima = {}
    clustered_exceedances = {}

    for k in data.columns:
        series = data[k]
        threshold = thresholds[k]
        exceedances = series[series > threshold]

        if exceedances.empty:
            cluster_maxima[k] = pd.DataFrame(columns=["Loss", "Cluster"])
            continue

        dates = exceedances.index
        values = exceedances.values

        cluster_id = np.zeros(len(exceedances), dtype=int)
        current_cluster = 0
        max_gap = pd.Timedelta(days=r)

        for i in range(1, len(exceedances)):
            if dates[i] - dates[i - 1] > max_gap:
                current_cluster += 1
            cluster_id[i] = current_cluster

        declustered_df = pd.DataFrame({"Loss": values, "Cluster": cluster_id}, index=dates)

        clustered_exceedances[k] = declustered_df
        idx = declustered_df.groupby("Cluster")["Loss"].idxmax()
        cluster_maxima[k] = declustered_df.loc[idx]

    return cluster_maxima, clustered_exceedances

