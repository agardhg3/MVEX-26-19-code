def staticFiltering(thresholds: dict[str, float], data: pd.DataFrame, window_size: int) -> dict:
    """
    Construct non-overlapping recovery windows after extreme losses.

    Parameters
    ----------
    thresholds : dict[str, float]
        Threshold values by stock.
    data : pd.DataFrame
        DataFrame generated by get_data().
    window_size : int
        Length of each recovery window in calendar days.

    Returns
    -------
    recovery_windows : dict
        Dictionary mapping stock name to a DataFrame with columns
        "start" and "end" for each recovery window.
    """
    if not isinstance(thresholds, dict):
        raise TypeError("thresholds must be a dictionary")
    elif not isinstance(data, pd.DataFrame):
        raise TypeError("data must be a DataFrame")
    elif not isinstance(window_size, int):
        raise TypeError("s must be an int")

    recovery_windows = {}

    for k in data.columns:
        series = data[k]
        threshold = thresholds[k]
        exceed_mask = series > threshold
        exceedances = series[exceed_mask]

        dates = exceedances.index
        window_length = pd.Timedelta(days=window_size)

        if len(dates) == 0:
            recovery_windows[k] = pd.DataFrame(columns=["start", "end"])
            continue

        pivot = dates[0]
        start = [pivot]
        end = [pivot + window_length]

        for i in range(1, len(dates)):
            if dates[i] - pivot > window_length:
                pivot = dates[i]
                start.append(pivot)
                end.append(pivot + window_length)

        recovery_windows[k] = pd.DataFrame({
            "start": start,
            "end": end
        })

    return recovery_windows


def Generate_Recovery_Data(data: pd.DataFrame, event_periods: dict[str, pd.DataFrame], stock: str, window_size: int = 10):
    """
    Generate recovery data after extreme losses.

    Parameters
    ----------
    data : pd.DataFrame
        DataFrame generated by get_data().
    event_periods : dict[str, pd.DataFrame]
        Dictionary mapping each stock to a DataFrame with columns
        "start" and "end". This can represent the recovery windows
        from staticFiltering().
    stock : str
        Stock name.
    window_size : int
        Fixed recovery horizon in days when dynamic=False.


    Returns
    -------
    recoveries : pd.DataFrame
        DataFrame containing loss and recovery values.
    partial_recovery : pd.DataFrame
        Events with partial recovery.
    full_recovery : pd.DataFrame
        Events with full recovery.
    no_recovery : pd.DataFrame
        Events with no recovery.
    recovery_ratio : float
        Fraction of full recoveries among all events.
    """
    if not isinstance(data, pd.DataFrame):
        raise TypeError("data must be a DataFrame")
    elif not isinstance(event_periods, dict):
        raise TypeError("event_periods must be a dictionary")
    elif not isinstance(stock, str):
        raise TypeError("stock must be a string")
    elif not isinstance(window_size, int):
        raise TypeError("window_size must be an int")

    if stock not in event_periods:
        raise KeyError(f"{stock} not found in event_periods")

    recovery_period = pd.Timedelta(window_size, "D")
    plus_one = pd.Timedelta("1D")
    starts = event_periods[stock]["start"]

    vals = [data[stock].loc[d + plus_one : d + plus_one + recovery_period].sum() for d in starts]
   
    recoveries = pd.DataFrame({f"{window_size} day recovery": vals, "Loss": data[stock].loc[starts].values}, index=starts)
    
    recovery_col = f"{window_size} day recovery"
    recovery_amount = -recoveries[recovery_col]
    loss = recoveries["Loss"]
    
    recovery_50plus = recoveries[(recovery_amount >= loss/2) & (recovery_amount<loss)]
    recovery_50minus = recoveries[(recovery_amount >= 0) & (recovery_amount<loss/2)]
    full_recovery = recoveries[recovery_amount >= loss]
    no_recovery = recoveries[recovery_amount < 0]

    n = len(starts)
    zero_ratio = len(no_recovery) / n
    ratio_50minus= len(recovery_50minus) / n
    ratio_50plus= len(recovery_50plus) / n 
    full_ratio = len(full_recovery) / n

    ratios = np.array([zero_ratio, ratio_50minus, ratio_50plus, full_ratio])

    return ratios, recoveries,  no_recovery, recovery_50minus, recovery_50plus, full_recovery

